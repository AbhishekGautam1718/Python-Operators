def assignmentoprators():
    x=int(input("Enter value for:"))
    x==5
    print("x==5",x)
    x+=5
    print("x+=:",x)
    x-=5
    print("x-=5:",x)
    x*=5
    print("x*=5:",x)
    x**=5
    print("x**=5:",x)
    x//=5
    print("x//=5:",x)
    x%=5
    print("x%=5:",x)
assignmentoprators()
